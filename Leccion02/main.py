operandoA=3
operandoB=2


#SUMA
suma=operandoA+operandoB

print('resultado de la suma es:',suma)
print(f'Resultado suma: {suma}')


#Resta
resta=operandoA-operandoB

print('resultado de la resta es:',resta)
print(f'Resultado resta: {resta}')

#multiplicacion
multiplicacion=operandoA*operandoB
print('resultado de la multiplicacion es:',multiplicacion)
print(f'Resultado multiplicacion: {multiplicacion}')

#division
division=operandoA/operandoB
print('resultado de la division es:',division)
print(f'Resultado division: {division}')


#division devuelve parte entera
division=operandoA//operandoB
print('resultado de la division (int):',division)
print(f'Resultado division (int): {division}')


#modulo o residuo
modulo=operandoA%operandoB
print('resultado de la modulo:',modulo)
print(f'Resultado modulo: {modulo}')

#exponente
exponente=operandoA**operandoB
print('resultado de la exponente:',exponente)
print(f'Resultado exponente: {exponente}')
